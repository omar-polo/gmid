#include <sys/queue.h>
#include <sys/uio.h>
#include <stdint.h>
#include <imsg.h>

int
main(void)
{
	struct imsgbuf buf;
	struct imsg imsg;

	if (imsgbuf_init(&buf, -1) == -1)
		return 1;
	imsgbuf_allow_fdpass(&ibuf);
	return imsg_get_fd(&imsg);
}
